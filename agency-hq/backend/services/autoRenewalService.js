/**
 * Auto-Renewal Service
 * Handles automatic top-ups and circuit breakers for the autonomous wallet
 */

const fs = require('fs');
const path = require('path');
const Database = require('better-sqlite3');

const DB_PATH = path.join(__dirname, '../db/agency.db');

// Auto-renewal rules
const AUTO_RENEW_RULES = [
  {
    vendor: 'Anthropic',
    trigger: 'api_credits < $20',
    action: 'purchase $100 credits',
    card: 'stackz-api',
    pool: 'operational',
    max_per_month: 3,
    last_renewal: null
  },
  {
    vendor: 'Ollama',
    trigger: 'remaining_tokens < 100000',
    action: 'purchase standard pack',
    card: 'stackz-api',
    pool: 'operational',
    max_per_month: 2,
    last_renewal: null
  },
  {
    vendor: 'Brave Search',
    trigger: 'remaining_requests < 1000',
    action: 'purchase next tier',
    card: 'stackz-api',
    pool: 'operational',
    max_per_month: 1,
    last_renewal: null
  }
];

// Circuit breakers
const CIRCUIT_BREAKERS = [
  {
    name: 'CRITICAL_BALANCE',
    trigger: 'operational_pool < (monthly_burn * 1)',
    actions: [
      'FREEZE_GROWTH',
      'FREEZE_TRIALS',
      'ALERT_CRITICAL'
    ]
  },
  {
    name: 'LOW_BALANCE_WARNING',
    trigger: 'operational_pool < (monthly_burn * 2)',
    actions: [
      'PAUSE_GROWTH',
      'DEFER_TRIALS',
      'ALERT_WARNING'
    ]
  },
  {
    name: 'SPEND_SPIKE',
    trigger: 'daily_spend > (avg_daily_spend * 3)',
    actions: [
      'FREEZE_DISCRETIONARY_24H',
      'AUDIT_EXPENSES'
    ]
  },
  {
    name: 'CAMPAIGN_ZERO_CONVERSION',
    trigger: 'campaign_spend > 0.3 * budget AND conversions == 0',
    actions: [
      'PAUSE_CAMPAIGN',
      'SMOKE_REVIEW'
    ]
  }
];

// State for frozen categories
let frozenCategories = new Set();
let lastCheck = null;

/**
 * Get database connection
 */
function getDb() {
  return new Database(DB_PATH);
}

/**
 * Get current pool balances
 */
async function getPoolBalances() {
  const db = getDb();
  try {
    const pools = db.prepare(`
      SELECT pool_name, balance, monthly_burn
      FROM wallet_pools
    `).all();

    const result = {};
    for (const pool of pools) {
      result[pool.pool_name] = {
        balance: pool.balance,
        monthly_burn: pool.monthly_burn || 0,
        months_remaining: pool.monthly_burn > 0
          ? pool.balance / pool.monthly_burn
          : Infinity
      };
    }
    return result;
  } finally {
    db.close();
  }
}

/**
 * Check if a category is frozen
 */
function isFrozen(category) {
  return frozenCategories.has(category);
}

/**
 * Freeze a spending category
 */
function freeze(category) {
  frozenCategories.add(category);
  console.log(`[AutoRenewal] FROZEN: ${category}`);
  logAlert('CATEGORY_FROZEN', { category, reason: 'circuit_breaker' });
}

/**
 * Unfreeze a spending category
 */
function unfreeze(category) {
  frozenCategories.delete(category);
  console.log(`[AutoRenewal] UNFROZEN: ${category}`);
}

/**
 * Log an alert to the Alerts folder
 */
function logAlert(type, details) {
  const alertsDir = path.join(__dirname, '../../docs/Finance/Alerts');
  if (!fs.existsSync(alertsDir)) {
    fs.mkdirSync(alertsDir, { recursive: true });
  }

  const date = new Date().toISOString().split('T')[0];
  const filename = `${date}-${type.toLowerCase()}.md`;
  const filepath = path.join(alertsDir, filename);

  const content = `# Alert: ${type}

**Date**: ${new Date().toISOString()}
**Type**: ${type}

## Details

\`\`\`json
${JSON.stringify(details, null, 2)}
\`\`\`

## Actions Taken

${details.actions || 'None'}

---

*Generated by Auto-Renewal Service*
`;

  fs.writeFileSync(filepath, content);
  console.log(`[AutoRenewal] Alert logged: ${filepath}`);
}

/**
 * Check and execute auto-renewals
 */
async function checkAutoRenewals() {
  const results = [];

  for (const rule of AUTO_RENEW_RULES) {
    // Check if we've hit the monthly limit
    if (rule.last_renewal) {
      const lastMonth = new Date(rule.last_renewal).getMonth();
      const thisMonth = new Date().getMonth();
      if (lastMonth === thisMonth) {
        continue; // Already renewed this month
      }
    }

    // Check if category is frozen
    if (isFrozen(rule.card)) {
      console.log(`[AutoRenewal] Skipping ${rule.vendor} - category frozen`);
      continue;
    }

    // TODO: Actually check the trigger condition via API
    // For now, this is a placeholder that would integrate with vendor APIs

    results.push({
      vendor: rule.vendor,
      action: rule.action,
      executed: false,
      reason: 'trigger_check_not_implemented'
    });
  }

  return results;
}

/**
 * Check circuit breakers
 */
async function checkCircuitBreakers() {
  const pools = await getPoolBalances();
  const triggered = [];

  // Check CRITICAL_BALANCE
  const op = pools.operational;
  if (op && op.months_remaining < 1) {
    triggered.push({
      name: 'CRITICAL_BALANCE',
      months_remaining: op.months_remaining
    });
  }

  // Check LOW_BALANCE_WARNING
  if (op && op.months_remaining < 2 && op.months_remaining >= 1) {
    triggered.push({
      name: 'LOW_BALANCE_WARNING',
      months_remaining: op.months_remaining
    });
  }

  // Execute triggered circuit breakers
  for (const trigger of triggered) {
    const breaker = CIRCUIT_BREAKERS.find(b => b.name === trigger.name);
    if (breaker) {
      for (const action of breaker.actions) {
        switch (action) {
          case 'FREEZE_GROWTH':
            freeze('growth');
            break;
          case 'FREEZE_TRIALS':
            freeze('trials');
            break;
          case 'PAUSE_GROWTH':
            // Soft pause - mark for review
            console.log(`[AutoRenewal] PAUSE: Growth pool - review needed`);
            break;
          case 'DEFER_TRIALS':
            freeze('trials');
            break;
          case 'ALERT_CRITICAL':
          case 'ALERT_WARNING':
            logAlert(trigger.name, {
              months_remaining: trigger.months_remaining,
              pool_balances: pools,
              actions: breaker.actions
            });
            break;
        }
      }
    }
  }

  lastCheck = new Date();
  return triggered;
}

/**
 * Main check function - runs all checks
 */
async function check() {
  console.log(`[AutoRenewal] Running checks at ${new Date().toISOString()}`);

  const autoRenewResults = await checkAutoRenewals();
  const circuitBreakerResults = await checkCircuitBreakers();

  return {
    checked_at: new Date().toISOString(),
    auto_renewals: autoRenewResults,
    circuit_breakers: circuitBreakerResults,
    frozen_categories: Array.from(frozenCategories)
  };
}

/**
 * Get current frozen status
 */
function getFrozenStatus() {
  return {
    frozen: Array.from(frozenCategories),
    last_check: lastCheck
  };
}

/**
 * Initialize the service - create tables if needed
 */
function initialize() {
  const db = getDb();

  // Create wallet_pools table if it doesn't exist
  db.exec(`
    CREATE TABLE IF NOT EXISTS wallet_pools (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      pool_name TEXT UNIQUE NOT NULL,
      balance REAL DEFAULT 0,
      monthly_burn REAL DEFAULT 0,
      created_at TEXT DEFAULT CURRENT_TIMESTAMP,
      updated_at TEXT DEFAULT CURRENT_TIMESTAMP
    )
  `);

  // Create initial pools if they don't exist
  const pools = ['operational', 'growth', 'reserve'];
  const insert = db.prepare(`
    INSERT OR IGNORE INTO wallet_pools (pool_name, balance, monthly_burn)
    VALUES (?, 0, 0)
  `);

  for (const pool of pools) {
    insert.run(pool);
  }

  // Create auto_renewal_log table
  db.exec(`
    CREATE TABLE IF NOT EXISTS auto_renewal_log (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      vendor TEXT NOT NULL,
      action TEXT NOT NULL,
      executed INTEGER DEFAULT 0,
      executed_at TEXT,
      created_at TEXT DEFAULT CURRENT_TIMESTAMP
    )
  `);

  db.close();
  console.log('[AutoRenewal] Service initialized');
}

module.exports = {
  initialize,
  check,
  checkAutoRenewals,
  checkCircuitBreakers,
  freeze,
  unfreeze,
  isFrozen,
  getFrozenStatus,
  getPoolBalances,
  AUTO_RENEW_RULES,
  CIRCUIT_BREAKERS
};